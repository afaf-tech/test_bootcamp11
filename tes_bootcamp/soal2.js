function requirePhoneNumber(phone){
  var pp = phone.indexOf('+62')
  if(pp>0){
    return 'Number phone tidak valid'
  }
  
  var ak = new RegExp(/^[0-9+ ]+$/);
  if(!Boolean(ak.test(phone))){
	return 'Number phone tidak valid'
  }
  
  var jp = phone.length
  if(jp<8){
	return 'Number phone tidak valid'
  }
  if(jp>15){
	return 'Number phone tidak valid'
  }
 return 'Number phone valid'
}
