function requireEmail(email) {

  for (var a = 0; a < email.length; a++) {
    if (email[a] !== email[a].toLowerCase()) {
      return "Email anda tidak valid";
    }
  }

  var at = email.indexOf('@')
  if(at > 1){
    return 'Email anda valid';
  } else {
    return 'Email anda tidak valid';
  }
   var dot = email.indexOf('.')
  if(dot > 1){
    return 'Email anda valid';
  } else {
    return 'Email anda tidak valid';
  }

}
